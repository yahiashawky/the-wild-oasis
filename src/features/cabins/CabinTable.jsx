function CabinTable() {
  const { cabins, isLoading } = useCabins();
  const [searchParams] = useSearchParams();

  if (isLoading) return <Spinner />;
  if (!cabins) return <Empty resourceName="cabins" />;

  // 1) Filtering
  const discountFilter = searchParams.get("discount") || "all";
  let filteredCabins = cabins;

  if (discountFilter === "all") filteredCabins = cabins;
  else if (discountFilter === "with-discount")
    filteredCabins = cabins.filter((cabin) => cabin.discount > 0);
  else if (discountFilter === "no-discount")
    filteredCabins = cabins.filter((cabin) => cabin.discount === 0);

  // 2) Sorting
  const sortBy = searchParams.get("sortBy") || "startDate-asc";
  const [field, direction] = sortBy.split("-");
  const modifier = direction === "asc" ? 1 : -1;
  const sortedCabins = filteredCabins.sort(
    (a, b) => (a[field] - b[field]) * modifier,
  );

  return (
    <Menus>
      <Table columns="0.6fr 1.8fr 2.2fr 1fr 1fr 1fr">
        <Table.Header>
          <div></div>
          <div>Cabin</div>
          <div>Capacity</div>
          <div>Price</div>
          <div>Discount</div>
          <div>Operations</div>
        </Table.Header>
        <Table.Body
          // data={cabins}
          // data={filteredCabins}
          data={sortedCabins}
          render={(cabin) => <CabinRow key={cabin.id} cabin={cabin} />}
        />
      </Table>
    </Menus>
  );
}

export default CabinTable;
